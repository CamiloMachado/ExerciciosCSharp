// Faça um programa que exiba os números de 1 até 2000.

namespace ExerciciosCSharp.Exercicios {

    internal class Exercicio29 {

        public static void Executar() {
            // Laço de 1 até 2000
            for (int i = 1; i < 2001; i++) {
                Console.WriteLine("{0}", i);
            }
        }
    }
}
